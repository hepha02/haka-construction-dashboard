create table if not exists public.payments (
  id bigint generated by default as identity primary key,
  store text not null,
  vendor text not null,
  payment_item text,
  estimate_total bigint,
  payment_type text not null default '일시 지급' check (payment_type in ('일시 지급', '선금 50%', '잔금 50%', '직접 입력')),
  amount bigint not null,
  vendor_bank text,
  vendor_account_number text,
  vendor_account_holder text,
  tax_type text not null default '일반 송금' check (tax_type in ('일반 송금', '사업소득 3.3%')),
  withholding_amount bigint not null default 0,
  net_amount bigint,
  attachment_files jsonb not null default '{}'::jsonb,
  estimate_group_mode text not null default '매장별 항목 합산',
  estimate_group_key text,
  memo text,
  status text not null check (status in ('승인', '신청', '반려')),
  requested_at date not null default current_date
);

create table if not exists public.stores (
  id bigint generated by default as identity primary key,
  region text,
  name text not null,
  fixture_count integer not null default 0,
  area integer not null,
  status text not null check (status in ('완료', '진행중', '미착공')),
  budget bigint not null,
  spent bigint not null default 0,
  document_required boolean not null default false,
  excel_row integer
);

create table if not exists public.construction_cost_parts (
  id bigint generated by default as identity primary key,
  store_name text not null,
  excel_row integer not null,
  part_name text not null,
  amount bigint not null default 0
);

create table if not exists public.store_quotes (
  id bigint generated by default as identity primary key,
  store_name text not null unique,
  quote_status text not null default '정산중' check (quote_status in ('정산중', '견적 확정', '계약 완료')),
  margin_rate numeric not null default 35,
  direct_cost bigint not null default 0,
  fixture_cost bigint not null default 0,
  cost_total bigint not null default 0,
  supply_amount bigint not null default 0,
  vat_amount bigint not null default 0,
  total_amount bigint not null default 0,
  quote_confirmed_at timestamptz,
  contract_completed_at timestamptz,
  updated_at timestamptz not null default now()
);

create table if not exists public.construction_starts (
  id bigint generated by default as identity primary key,
  store_name text not null,
  area integer not null,
  drawing_note text,
  drawing_files jsonb not null default '[]'::jsonb,
  wall_cabinet_count integer not null default 0,
  display_fixture_count integer not null default 0,
  counter_count integer not null default 0,
  fixture_count integer not null default 0,
  table_count integer not null default 0,
  sign_count integer not null default 0,
  base_photo_note text,
  base_photo_files jsonb not null default '[]'::jsonb,
  special_notes text,
  created_at timestamptz not null default now()
);

