create policy "authenticated insert stores"
on public.stores
for insert
with check (
  auth.role() = 'authenticated'
  and length(trim(name)) > 0
  and area > 0
  and budget > 0
  and spent >= 0
  and status in ('완료', '진행중', '미착공')
);

create policy "authenticated insert vendors"
on public.vendors
for insert
with check (
  auth.role() = 'authenticated'
  and length(trim(name)) > 0
  and length(trim(category)) > 0
  and length(trim(bank)) > 0
  and length(trim(account_number)) > 0
  and length(trim(account_holder)) > 0
  and risk = '정상'
  and total = 0
);

create policy "authenticated insert payments"
on public.payments
for insert
with check (
  auth.role() = 'authenticated'
  and status = '신청'
  and amount > 0
  and estimate_total > 0
  and withholding_amount >= 0
  and net_amount > 0
  and length(trim(store)) > 0
  and length(trim(vendor)) > 0
  and length(trim(vendor_bank)) > 0
  and length(trim(vendor_account_number)) > 0
  and length(trim(vendor_account_holder)) > 0
  and length(trim(payment_item)) > 0
  and payment_type in ('일시 지급', '선금 50%', '잔금 50%', '직접 입력')
  and tax_type in ('일반 송금', '사업소득 3.3%')
  and length(trim(estimate_group_key)) > 0
);

create policy "authenticated insert construction starts"
on public.construction_starts
for insert
with check (
  auth.role() = 'authenticated'
  and length(trim(store_name)) > 0
  and area > 0
  and wall_cabinet_count >= 0
  and display_fixture_count >= 0
  and counter_count >= 0
  and fixture_count >= 0
  and table_count >= 0
  and sign_count >= 0
);

create policy "authenticated upsert store quotes"
on public.store_quotes
for insert
with check (
  auth.role() = 'authenticated'
  and length(trim(store_name)) > 0
  and quote_status in ('정산중', '견적 확정', '계약 완료')
  and margin_rate >= 0
  and cost_total >= 0
  and supply_amount >= 0
  and vat_amount >= 0
  and total_amount >= 0
);

create policy "authenticated update store quotes"
on public.store_quotes
for update
using (auth.role() = 'authenticated')
with check (
  auth.role() = 'authenticated'
  and length(trim(store_name)) > 0
  and quote_status in ('정산중', '견적 확정', '계약 완료')
  and margin_rate >= 0
  and cost_total >= 0
  and supply_amount >= 0
  and vat_amount >= 0
  and total_amount >= 0
);

create policy "authenticated update pending payments"
on public.payments
for update
using (auth.role() = 'authenticated' and status = '신청')
with check (auth.role() = 'authenticated' and status in ('승인', '반려'));

