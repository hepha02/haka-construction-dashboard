create table if not exists public.user_roles (
  id bigint generated by default as identity primary key,
  email text not null unique,
  role text not null check (role in ('admin', 'interior_manager')),
  created_at timestamptz not null default now()
);

create table if not exists public.vendors (
  id bigint generated by default as identity primary key,
  name text not null,
  category text not null,
  bank text not null,
  account_number text,
  account_holder text,
  risk text not null default '정상',
  total bigint not null default 0
);

alter table public.payments enable row level security;
alter table public.stores enable row level security;
alter table public.vendors enable row level security;
alter table public.construction_cost_parts enable row level security;
alter table public.construction_starts enable row level security;
alter table public.user_roles enable row level security;

create policy "authenticated read payments" on public.payments for select using (auth.role() = 'authenticated');
create policy "authenticated read stores" on public.stores for select using (auth.role() = 'authenticated');
create policy "authenticated read vendors" on public.vendors for select using (auth.role() = 'authenticated');
create policy "authenticated read construction cost parts" on public.construction_cost_parts for select using (auth.role() = 'authenticated');
create policy "authenticated read construction starts" on public.construction_starts for select using (auth.role() = 'authenticated');
create policy "users read own role" on public.user_roles for select using (lower(email) = lower(auth.jwt() ->> 'email'));

create policy construction_files_read
on storage.objects
for select
using (bucket_id = 'construction-start-files' and auth.role() = 'authenticated');

create policy construction_files_insert
on storage.objects
for insert
with check (bucket_id = 'construction-start-files' and auth.role() = 'authenticated');

create policy "authenticated insert stores"
on public.stores
for insert
with check (
  auth.role() = 'authenticated'
  and length(trim(name)) > 0
  and area > 0
  and budget > 0
  and spent >= 0
  and status in ('완료', '진행중', '미착공')
);

create policy "authenticated insert vendors"
on public.vendors
for insert
with check (
  auth.role() = 'authenticated'
  and length(trim(name)) > 0
  and length(trim(category)) > 0
  and length(trim(bank)) > 0
  and length(trim(account_number)) > 0
  and length(trim(account_holder)) > 0
  and risk = '정상'
  and total = 0
);

create policy "authenticated insert payments"
on public.payments
for insert
with check (
  auth.role() = 'authenticated'
  and status = '신청'
  and amount > 0
  and length(trim(store)) > 0
  and length(trim(vendor)) > 0
);

create policy "authenticated insert construction starts"
on public.construction_starts
for insert
with check (
  auth.role() = 'authenticated'
  and length(trim(store_name)) > 0
  and area > 0
  and fixture_count >= 0
  and table_count >= 0
  and sign_count >= 0
);

create policy "authenticated update pending payments"
on public.payments
for update
using (auth.role() = 'authenticated' and status = '신청')
with check (auth.role() = 'authenticated' and status in ('승인', '반려'));

