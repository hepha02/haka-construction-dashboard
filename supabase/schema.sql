create table if not exists public.user_roles (
  id bigint generated by default as identity primary key,
  email text not null unique,
  role text not null check (role in ('admin', 'interior_manager')),
  created_at timestamptz not null default now()
);

create or replace function public.assign_default_interior_manager_role()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.user_roles (email, role)
  values (new.email, 'interior_manager')
  on conflict (email) do nothing;

  return new;
end;
$$;

drop trigger if exists assign_default_interior_manager_role on auth.users;
create trigger assign_default_interior_manager_role
after insert on auth.users
for each row
execute function public.assign_default_interior_manager_role();

