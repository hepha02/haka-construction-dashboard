create table if not exists public.payments (
  id bigint generated by default as identity primary key,
  store text not null,
  vendor text not null,
  amount bigint not null,
  status text not null check (status in ('승인', '신청', '반려')),
  requested_at date not null default current_date
);

create table if not exists public.stores (
  id bigint generated by default as identity primary key,
  region text,
  name text not null,
  fixture_count integer not null default 0,
  area integer not null,
  status text not null check (status in ('완료', '진행중', '미착공')),
  budget bigint not null,
  spent bigint not null default 0,
  document_required boolean not null default false,
  excel_row integer
);

create table if not exists public.construction_cost_parts (
  id bigint generated by default as identity primary key,
  store_name text not null,
  excel_row integer not null,
  part_name text not null,
  amount bigint not null default 0
);

create table if not exists public.construction_starts (
  id bigint generated by default as identity primary key,
  store_name text not null,
  area integer not null,
  drawing_note text,
  drawing_files jsonb not null default '[]'::jsonb,
  fixture_count integer not null default 0,
  table_count integer not null default 0,
  sign_count integer not null default 0,
  base_photo_note text,
  base_photo_files jsonb not null default '[]'::jsonb,
  special_notes text,
  created_at timestamptz not null default now()
);

